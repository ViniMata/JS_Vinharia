function verificarIdade(idadeMinima) {
    var idade = prompt("Digite sua idade:");
    if (idade >= idadeMinima) {
        window.location.href = "/html/login.html";
    } else {
        window.location.href = "https://encurtador.com.br/pyGN0";
    }
}

